static void udp_client_netconn_thread(void *arg)
{

	int socket_fd;
	struct sockaddr_in sa,ra;

	 const TickType_t xDelay = 1000 ;
	int sent_data; char recv_data_buffer[80];
	/* Creates an UDP socket (SOCK_DGRAM) with Internet Protocol Family (PF_INET).
	 * Protocol family and Address family related. For example PF_INET Protocol Family and AF_INET family are coupled.
	*/
	socket_fd = socket(AF_INET, SOCK_DGRAM, 0);

	if ( socket_fd < 0 )
	{

	    printf("socket call failed");
	    exit(0);

	}

	memset(&sa, 0, sizeof(struct sockaddr_in));
	sa.sin_family = AF_INET;
	sa.sin_addr.s_addr = INADDR_ANY;
	sa.sin_port = htons(17225);


	/* Bind the TCP socket to the port SENDER_PORT_NUM and to the current
	* machines IP address (Its defined by SENDER_IP_ADDR).
	* Once bind is successful for UDP sockets application can operate
	* on the socket descriptor for sending or receiving data.
	*/
	if (bind(socket_fd, (struct sockaddr *)&sa, sizeof(struct sockaddr_in)) == -1)
	{
	printf("Bind to Port Number %d ,IP address %s failed\n",17225,"192.168.1.4");
	close(socket_fd);
	exit(1);
	}

	memset(&ra, 0, sizeof(struct sockaddr_in));
	ra.sin_family = AF_INET;
	ra.sin_addr.s_addr = INADDR_ANY;
	ra.sin_port = htons(17225);

	uint8_t  buffer[256];

	unsigned int addr_size = sizeof(ra);

	while(1)
	{
		int msgLen = recvfrom(socket_fd,buffer,256,0,(struct sockaddr *)&ra,(socklen_t *)&addr_size);

		struct sockaddr_in destaddr = {
							.sin_family = AF_INET,
							.sin_port = ra.sin_port,
							//.sin_addr ={.s_addr = inet_addr("224.3.3.3")},
							.sin_addr   = {.s_addr = ra.sin_addr.s_addr},
							.sin_zero   = {0},
						};

		vTaskDelay(xDelay);
		sent_data = sendto(socket_fd, buffer,sizeof("Hello World"),0,(struct sockaddr*)&destaddr,sizeof(destaddr));
		if(sent_data < 0)
		{

			printf("send failed\n");
			close(socket_fd);
			exit(2);

		}
		vTaskDelay(xDelay);
	}

	close(socket_fd);


}

void udp_client_netconn_init()
{
	sys_thread_new("UDP", udp_client_netconn_thread,NULL,DEFAULT_THREAD_STACKSIZE,UDPCLIENT_THREAD_PRIO);
}
